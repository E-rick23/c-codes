//This code simply makes the addition operation of two numbers.
#include <stdio.h>

int main()
{
     //Declaring variables.
     int a, b, x;
     //Waiting for the input of two values.
     scanf("%d%d", &a, &b);
     //Addition operation.
     x = a+b;
     //Printing the result.
     printf("X = %d\n", x);
    return 0;
}
